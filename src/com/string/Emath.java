package com.string;

public class Emath {
	public static void main(String[] args) {
		System.out.println("计算绝对值的结果"+Math.abs(-10));
		System.out.println("计算正弦结果"+Math.sin(5));
		System.out.println("计算余弦结果"+Math.cos(10));
		System.out.println("计算正切结果"+Math.tan(1));
		System.out.println("计算平方根结果"+Math.sqrt(9));
		System.out.println("计算立方根结果"+Math.cbrt(8));
		System.out.println("计算乘方的结果"+Math.pow(2, 2));
		System.out.println("求大于参数的最小整数"+Math.ceil(4.6));
		System.out.println("求小于参数的最大整数"+Math.floor(-5.2));
		System.out.println("对小数进行四舍五入的结果"+Math.round(-4.6)); 
		System.out.println("求俩个数的最大值"+Math.max(5.1, 5.6));
		System.out.println("求俩个数的最小值"+Math.min(5.1, 5.6));
		System.out.println("生成一个随机数"+Math.random());
	}

}
